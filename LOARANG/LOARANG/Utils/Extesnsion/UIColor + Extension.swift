    static let buttonColor = UIColor(named: "buttonColor") ?? .clear
    static let tableViewColor = UIColor(named: "tableViewColor") ?? .clear
    static let cellBackgroundColor = UIColor(named: "cellBackgroundColor") ?? .clear
    static let cellColor = UIColor(named: "cellColor") ?? .clear
}
